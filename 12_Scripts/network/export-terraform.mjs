import fs from "fs";
import path from "path";

const networkDir = path.resolve("11_DataModels/data/network");
const inputFile = path.join(networkDir, "output.json");
const outputDir = path.resolve("10_Artifacts/network");
const tfFile = path.join(outputDir, "network-main.tf");

const design = JSON.parse(fs.readFileSync(inputFile, "utf8"));
fs.mkdirSync(outputDir, { recursive: true });

function safeName(name) {
  return name.replace(/[^a-zA-Z0-9_]/g, "_");
}

let tf = `# Generated by ArchToolKit Network Infrastructure Design\n\n`;

for (const vpc of design.vpcs || []) {
  const vpcName = safeName(`${vpc.environment}_vpc`);
  tf += `resource "aws_vpc" "${vpcName}" {\n`;
  tf += `  cidr_block = "${vpc.cidr}"\n`;
  tf += `  tags = { Name = "${vpc.environment}-vpc" }\n`;
  tf += `}\n\n`;

  if (vpc.internet_gateway) {
    tf += `resource "aws_internet_gateway" "${vpcName}_igw" {\n`;
    tf += `  vpc_id = aws_vpc.${vpcName}.id\n`;
    tf += `  tags = { Name = "${vpc.environment}-igw" }\n`;
    tf += `}\n\n`;
  }

  for (const subnet of vpc.subnets || []) {
    const subnetName = safeName(subnet.name);
    tf += `resource "aws_subnet" "${subnetName}" {\n`;
    tf += `  vpc_id     = aws_vpc.${vpcName}.id\n`;
    tf += `  cidr_block = "${subnet.cidr}"\n`;
    tf += `  tags = { Name = "${subnet.name}" }\n`;
    tf += `}\n\n`;
  }
}

fs.writeFileSync(tfFile, tf);
console.log(`Wrote ${tfFile}`);
