import path from "path";
import fs from "fs";
import { readJson, writeText, ensureDir, ts } from "./common.mjs";

const ROOT = path.resolve(process.cwd());
const IN_DIR = path.join(ROOT, "data/imports/provider-inventory");
const OUT_DIR = path.join(ROOT, "web/cloud-decision-logic-kit/data");

function load(provider){
  const fp = path.join(IN_DIR, `${provider}.normalized.json`);
  if(!fs.existsSync(fp)) return null;
  return readJson(fp);
}

function makeJs(providerId, label, services){
  const svcObjs = services.map(s=>({ id:s.serviceId, category:"INVENTORY", name:s.name }));
  const payload = {
    meta:{ generatedAt: ts(), source:"provider-inventory-normalized" },
    serviceCategories:{
      inventory:{ label:`${label} - ALL SERVICES (Inventory)`, services: svcObjs }
    }
  };

  return `// AUTO-GENERATED by scripts/catalog/build-provider-js.mjs\n` +
         `window.CDK = window.CDK || {};\n` +
         `window.CDK.providers = window.CDK.providers || {};\n` +
         `window.CDK.providers.${providerId} = ${JSON.stringify(payload,null,2)};\n`;
}

function writeProvider(providerId, label, outName){
  const data = load(providerId);
  if(!data){ console.warn(`[build] missing normalized file for ${providerId}`); return; }
  const services = Array.isArray(data.services)?data.services:[];
  ensureDir(OUT_DIR);
  writeText(path.join(OUT_DIR,outName), makeJs(providerId,label,services));
  console.log(`[build] ${outName}: services=${services.length}`);
}

writeProvider("aws","AWS","aws-services.js");
writeProvider("azure","Azure","azure-services.js");
writeProvider("gcp","GCP","gcp-services.js");
writeProvider("oci","OCI","oci-services.js");
