import fs from "node:fs";
import path from "node:path";

function die(msg){ console.error("❌ "+msg); process.exit(1); }
function toRel(p){ return path.relative(process.cwd(), p).replace(/\\/g,"/"); }
function readJson(p){ try{ return JSON.parse(fs.readFileSync(p,"utf8")); } catch(e){ die(`Invalid JSON: ${toRel(p)}\n${e.message}`);} }
function ensureDir(p){ fs.mkdirSync(p,{recursive:true}); }
function writeFile(p,s){ ensureDir(path.dirname(p)); fs.writeFileSync(p,s,"utf8"); console.log("📝 wrote:", toRel(p)); }
function parseArgs(argv){
  const args=argv.slice(2); const out={inPath:null,outDir:null};
  const inIdx=args.indexOf("--in"); if(inIdx!==-1&&args[inIdx+1]) out.inPath=args[inIdx+1];
  const outIdx=args.indexOf("--outdir"); if(outIdx!==-1&&args[outIdx+1]) out.outDir=args[outIdx+1];
  if(!out.inPath&&args[0]&&!args[0].startsWith("-")) out.inPath=args[0];
  if(!out.outDir&&args[1]&&!args[1].startsWith("-")) out.outDir=args[1];
  return out;
}

function main(){
  const {inPath:inArg,outDir:outArg}=parseArgs(process.argv);
  if(!inArg) die("Usage: npm run export:tf:oci:workloads -- <enriched.json> [infra/oci/<export>]");
  const repoRoot=process.cwd();
  const inPath=path.resolve(repoRoot,inArg);
  if(!fs.existsSync(inPath)) die(`Input file not found: ${toRel(inPath)}`);

  const enriched=readJson(inPath);
  const exportName=enriched?.meta?.export_name||"manual";
  const outDir=outArg?path.resolve(repoRoot,outArg):path.resolve(repoRoot,"infra","oci",exportName);
  ensureDir(outDir);

  const inputs=enriched?.inputs||{};
  const internetExposed=!!inputs?.internet_exposed;
  const dataClass=Array.isArray(inputs?.data_classification)?inputs.data_classification:[];
  const strict=dataClass.includes("CJIS");

  const tf=`# Generated by ATK: OCI workload intent (NSG intent only)
# No compute is created. Safe to validate offline.

variable "deploy_workloads" { type = bool default = false }

locals {
  atk_internet_exposed = ${internetExposed ? "true" : "false"}
  atk_strict           = ${strict ? "true" : "false"}
}

# One NSG per VCN (attach to VNICs later)
resource "oci_core_network_security_group" "workload_default" {
  for_each       = oci_core_vcn.vcn
  compartment_id = var.compartment_ocid
  vcn_id         = each.value.id
  display_name   = "\${var.naming_prefix}-\${each.key}-workload-default"
}

# Optional: allow HTTPS inbound if internet_exposed
resource "oci_core_network_security_group_security_rule" "allow_https" {
  for_each = local.atk_internet_exposed ? oci_core_network_security_group.workload_default : {}

  network_security_group_id = each.value.id
  direction                 = "INGRESS"
  protocol                  = "6" # TCP

  source      = "0.0.0.0/0"
  source_type = "CIDR_BLOCK"

  tcp_options {
    destination_port_range {
      min = 443
      max = 443
    }
  }
}
`;

  const summary={
    export:"oci-workloads",
    export_name: exportName,
    internet_exposed: internetExposed,
    data_classification: dataClass,
    strict_defaults: strict,
    notes:["NSG intent only. Attach to VNICs later."]
  };

  writeFile(path.join(outDir,"workloads.tf"), tf);
  writeFile(path.join(outDir,"atk-plan.workloads.json"), JSON.stringify(summary,null,2));
  console.log("✅ Workload intent export complete.");
}

main();
