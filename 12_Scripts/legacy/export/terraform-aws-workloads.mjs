import fs from "node:fs";
import path from "node:path";

function die(msg) { console.error("❌ " + msg); process.exit(1); }
function toRel(p) { return path.relative(process.cwd(), p).replace(/\\/g, "/"); }
function readJson(p) {
  try { return JSON.parse(fs.readFileSync(p, "utf8")); }
  catch (e) { die(`Invalid JSON: ${toRel(p)}\n${e.message}`); }
}
function ensureDir(p) { fs.mkdirSync(p, { recursive: true }); }
function writeFile(p, s) { ensureDir(path.dirname(p)); fs.writeFileSync(p, s, "utf8"); console.log("📝 wrote:", toRel(p)); }
function parseArgs(argv) {
  const args = argv.slice(2);
  const out = { inPath: null, outDir: null };
  const inIdx = args.indexOf("--in"); if (inIdx !== -1 && args[inIdx + 1]) out.inPath = args[inIdx + 1];
  const outIdx = args.indexOf("--outdir"); if (outIdx !== -1 && args[outIdx + 1]) out.outDir = args[outIdx + 1];
  if (!out.inPath && args[0] && !args[0].startsWith("-")) out.inPath = args[0];
  if (!out.outDir && args[1] && !args[1].startsWith("-")) out.outDir = args[1];
  return out;
}

function main() {
  const { inPath: inArg, outDir: outArg } = parseArgs(process.argv);
  if (!inArg) die("Usage: npm run export:tf:aws:workloads -- <enriched.json> [infra/aws/<export>]");
  const repoRoot = process.cwd();
  const inPath = path.resolve(repoRoot, inArg);
  if (!fs.existsSync(inPath)) die(`Input file not found: ${toRel(inPath)}`);

  const enriched = readJson(inPath);
  const exportName = enriched?.meta?.export_name || "manual";
  const outDir = outArg ? path.resolve(repoRoot, outArg) : path.resolve(repoRoot, "infra", "aws", exportName);
  ensureDir(outDir);

  const inputs = enriched?.inputs || {};
  const internetExposed = !!inputs?.internet_exposed;
  const dataClass = Array.isArray(inputs?.data_classification) ? inputs.data_classification : [];
  const strict = dataClass.includes("CJIS");

  const tf = `# Generated by ATK: AWS workload intent (security groups only)
# NOTE: This does not create compute by default. Safe to validate offline.

variable "deploy_workloads" { type = bool default = false }

locals {
  atk_internet_exposed = ${internetExposed ? "true" : "false"}
  atk_strict           = ${strict ? "true" : "false"}
}

# Example: baseline workload SG (no ingress, egress all)
resource "aws_security_group" "workload_default" {
  name        = "\${var.naming_prefix}-workload-default"
  description = "ATK default workload SG"
  vpc_id      = aws_vpc.vpc[0].id
}

resource "aws_vpc_security_group_egress_rule" "workload_default_all" {
  security_group_id = aws_security_group.workload_default.id
  cidr_ipv4         = "0.0.0.0/0"
  ip_protocol       = "-1"
}

# Optional: internet-facing ALB SG intent
resource "aws_security_group" "alb_public" {
  count       = local.atk_internet_exposed ? 1 : 0
  name        = "\${var.naming_prefix}-alb-public"
  description = "ATK public ALB SG"
  vpc_id      = aws_vpc.vpc[0].id
}

resource "aws_vpc_security_group_ingress_rule" "alb_https" {
  count             = local.atk_internet_exposed ? 1 : 0
  security_group_id = aws_security_group.alb_public[0].id
  cidr_ipv4         = "0.0.0.0/0"
  from_port         = 443
  to_port           = 443
  ip_protocol       = "tcp"
}

resource "aws_vpc_security_group_egress_rule" "alb_all" {
  count             = local.atk_internet_exposed ? 1 : 0
  security_group_id = aws_security_group.alb_public[0].id
  cidr_ipv4         = "0.0.0.0/0"
  ip_protocol       = "-1"
}
`;

  const summary = {
    export: "aws-workloads",
    export_name: exportName,
    internet_exposed: internetExposed,
    data_classification: dataClass,
    strict_defaults: strict,
    notes: [
      "Security-group intent only (no compute).",
      "Enable compute in a later step when you want instance/ASG generation."
    ]
  };

  writeFile(path.join(outDir, "workloads.tf"), tf);
  writeFile(path.join(outDir, "atk-plan.workloads.json"), JSON.stringify(summary, null, 2));
  console.log("✅ Workload intent export complete.");
}

main();
